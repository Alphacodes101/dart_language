//* SWITCH CASE
/*

 switch case statements are a simplified version o
of the nested if else statement 

 */

void main() {
  int alphacodes = 23;

  switch (alphacodes) {
    case 1:
      {
        print('alpha numebr 1');
      }
      break;

    case 2:
      {
        print('alpha numebr 2');
      }
      break;
    case 3:
      {
        print('alpha numebr 3');
      }
      break;
    case 4:
      {
        print('alpha numebr 4');
      }
      break;

    default:
      {
        print('alpha numebr not found');
      }
  }
}
